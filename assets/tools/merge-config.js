const out = { byButtonId: {}, byServiceName: {} };
Object.assign(out.byButtonId, b.byButtonId||{});
Object.assign(out.byButtonId, a.byButtonId||{});
Object.assign(out.byServiceName, b.byServiceName||{});
Object.assign(out.byServiceName, a.byServiceName||{});
fs.writeFileSync('assets/data/services-config.json', JSON.stringify(out,null,2));
